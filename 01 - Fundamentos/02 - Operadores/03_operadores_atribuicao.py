a = 10 # Atribuição simples
print(f'a = {a}')

b = 10
b += 10 # Atribuição com adição
print(f'b = {b}')

c = 10
c -= 10 # Atribuição com subtração
print(f'c = {c}')

d = 10
d /= 10 # Atribuição com divisão
print(f'd = {d}')

e = 10
e //= 10 # Atribuição com divisão inteira
print(f'e = {e}')

f = 10
f %= 10 # Atribuição com módulo
print(f'f = {f}')

g = 10
g *= 10 # Atribuição com multiplicação
print(f'g = {g}')

h = 10
h **= 10 # Atribuição com potenciação
print(f'h = {h}')
